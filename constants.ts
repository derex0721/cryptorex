const generateHistory = (basePrice: number, volatility: number) => {
  const history = [];
  let price = basePrice;
  for (let i = 0; i < 24; i++) {
    price = price * (1 + (Math.random() * volatility - volatility / 2));
    history.push({
      time: `${i}:00`,
      price: price
    });
  }
  return history;
};

